let dropList = document.querySelectorAll("form select");
let fromCurrency = document.querySelector(".from select");
let toCurrency = document.querySelector(".to select");
let icon = document.querySelector(".icon");
let exchangeTxt = document.querySelector(".change_rate");
let getBtn = document.querySelector("button");

for (let i = 0; i < dropList.length; i++) {
  for (let currency_code in country_code) {
    let selected =
      i == 0
        ? currency_code == "NPR"
          ? "selected"
          : ""
        : currency_code == "USD"
        ? "selected"
        : "";

    let optionTag = `<option value="${currency_code}" ${selected}>${currency_code}</option>`;
    dropList[i].insertAdjacentHTML("beforeend", optionTag);
  }
  dropList[i].addEventListener("change", (e) => {
    loadFlag(e.target);
  });
}

function loadFlag(element) {
  for (let code in country_code) {
    if (code == element.value) {
      let imgTag = element.parentElement.querySelector("img");
      imgTag.src = `https://flagcdn.com/32x24/${country_code[
        code
      ].toLowerCase()}.png`;
    }
  }
}

getBtn.addEventListener("click", (e) => {
  e.preventDefault();
  getExchangeValue(country_code);
});

function getExchangeValue() {
  const amount = document.querySelector("form input");
  let amountVal = amount.value;
  if (amountVal == "" || amountVal == "0") {
    amount.value = "1";
    amountVal = 1;
  }
}
function getExchangeValue() {
  exchangeTxt.innerHTML = "Getting exchange rate...";
  let url = `https://api.currencyapi.com?apikey=cur_live_IBtLdoZ3iAWuz7uwLnLR6S9WSt29yjMoPGelwxqy?base_currency=${fromCurrency.value}&symbols=${toCurrency.value}`;

  console.log("Fetching data from URL:", url);
  fetch(url)
    .then((response) => {
      console.log("Response status:", response.status);
      // if (!response.ok) {
      //   throw new Error(`HTTP error! status: ${response.status}`);
      // }
      return response.json();
    })
    .then((result) => {
      console.log("API response:", result);

      if (result.data) {
        if (result.data[toCurrency.value]) {
          let exchangeRate = result.data[toCurrency.value].value;
          let total = (exchangeRate * amountVal).toFixed(2);
          exchangeTxt.innerHTML = `${exchangeRate} ${fromCurrency.value} = ${total} ${toCurrency.value}`;
        } else {
          console.error(
            `Currency ${toCurrency.value} not found. Available currencies:`,
            Object.keys(result.data)
          );
          throw new Error(
            `Currency ${toCurrency.value} not found in the data.`
          );
        }
      } else {
        console.log("Invalid data structure from API.");
      }
    })
    .catch((error) => {
      console.error("Error fetching exchange rate:", error);
      exchangeTxt.innerHTML = "Something went wrong!";
    });
}

window.addEventListener("load", () => {
  getExchangeValue(country_code);
});

icon.addEventListener("click", () => {
  let tempCode = fromCurrency.value;
  fromCurrency.value = toCurrency.value;
  toCurrency.value = tempCode;
  loadFlag(fromCurrency);
  loadFlag(toCurrency);
  getExchangeValue();
});
